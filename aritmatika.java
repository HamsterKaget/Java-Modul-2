public class aritmatika  {

    // code by Radja Aulia Al Ramdani X RPL 2
    public static void main(String[] args) {
        // Input / Variabel
        int a = 10;
        int b = 12;
        int c = 2;

        // Operasi Aritmatika 
        int tambah = a + b; // Pertambahan
        int kurang = b - a; // Pengurangan
        int diviso = a / c; // diviso
        int kali = a * b; // Perkalian
        int modulo = a % c; // Modulo / Sisa Bagi

        // Print data / hasil operasi aritmatika
        System.out.println(tambah); // 10 + 12 = 22
        System.out.println(kurang); // 12 - 10 = 2
        System.out.println(diviso); // 10 / 2 = 5
        System.out.println(kali); // 10 * 12 = 120
        System.out.println(modulo); // 10 % 2 = 0
    }
}